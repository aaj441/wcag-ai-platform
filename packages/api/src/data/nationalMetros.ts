export function getMetroByState(state: string) {
  return NATIONAL_METROS.filter(m => m.state === state);
}

export function getMetroById(metroId: string) {
  return NATIONAL_METROS.find(m => m.metroId === metroId);
}

export function getTopMetros(count: number = 50) {
  return NATIONAL_METROS.slice(0, count);
}

export function searchMetros(query: string) {
  const q = query.toLowerCase();
  return NATIONAL_METROS.filter(m =>
    m.name.toLowerCase().includes(q) ||
    m.metroId.includes(q)
  );
}
